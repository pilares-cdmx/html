           <p> En cada PILARES hay una ciberescuela, no sólo para consultar internet, sino para continuar estudios escolares que hayas dejado inconclusos, desde primaria, secundaria, preparatoria, y/o hasta carreras técnicas; siempre con el acompañamiento de promotores educativos (instructores y maestros) que enriquecerán tu aprendizaje.</p>
            
            
