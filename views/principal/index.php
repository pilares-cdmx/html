<p>A la par que las ciberescuelas también hay cursos de computación, programación, creación de aplicaciones móviles, y hasta cursos de reparación de teléfonos móviles.</p>
<p>En cada PILARES hay una red gratuita de internet para que dentro y fuera de las instalaciones cualquier persona se pueda conectar.</p>
<p>En todos los PILARES hay talleres de emprendimiento y cursos de educación para la autonomía económica, todos impartidos por expertos para que aprendas: diseño de modas y textiles, imagen y cosmetología, gastronomía y panadería, o cáterin; también se imparten cursos de plomería, electricidad y reparación de electrodomésticos, estos cursos son abiertos a hombres, mujeres, jóvenes o personas adultas mayores, la idea es que eventualmente pongas tu propio negocio.</p>     
        
<p>Uno de los objetivos es crear junto con las personas que tomen estos cursos, cooperativas o micro empresas, a las que el Gobierno de la Ciudad de México respaldará económicamente con apoyos tipo capital semilla o “a fondo perdido”, para echar a andar sus proyectos laborales colectivos.</p>

<p>En los PILARES también hay promotores deportivos para asesorarte en tu desarrollo físico, podrás crear equipos de futbol, basquetbol, voleibol, o hasta lucha, o si lo deseas puedes ser competidor individual en artes marciales, boxeo o atletismo para participar en torneos que se organizarán entre colonias, barrios y alcaldías; queremos organizar junto con todas y todos los habitantes de la ciudad las “Olimpiadas Comunitarias”, su objetivo es crear un ambiente deportivo sano que una a la ciudad a través del deporte.</p>

<p>Si lo que te interesa es el arte y la cultura, en los PILARES están los promotores culturales, para ofrecerte talleres artísticos de pintura, danza, música y dibujo, foto digital, teatro; el objetivo es organizar festivales comunitarios y de barrio, para unir a las distintas colonias de las alcaldías en eventos culturales y recreativos seguros, familiares y entretenidos.</p>

<p>También habrá en los PILARES, clubes de cine y/o literatura, y talleres de radio, de manualidades; buscamos impulsar el potencial creativo, y el talento de las y los habitantes de nuestra ciudad.</p>

<p>Recuerda que todas las actividades de los PILARES son gratuitas.</p>

<p>Queremos que todas y todos encuentren en los PILARES un espacio cultural, recreativo, donde puedan acercarse y encontrar algo que hacer y que aprender, queremos rescatar la vida comunitaria sana en la que la participación de los y las habitantes de la ciudad haga crecer a los PILARES.</p>
