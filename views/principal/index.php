                                <p>El día 28 de marzo se cumplen dos meses de la inauguración del primer PILARES, “Richard Wagner” en la Ciudad de México. En su escaso tiempo de funcionamiento, en este espacio ya se empiezan a tejer las primeras historias y anécdotas que confirman la formación de una comunidad que ejerce sus derechos: el derecho a la educación, a la cultura, al deporte y al uso del espacio público.</p>
                                 <p>Uno de estos casos es el de Alexis y Lalo, vecinos de la colonia; ellos son dos amigos que se conocieron desde la primaria; su gusto por el baile los llevó hasta el “Richard Wagner”, donde encontraron algo más que un espacio para llevar a cabo esta actividad. Días antes de la inauguración, Alexis y Lalo acudieron con los coordinadores del PILARES para pedir que se les prestara las instalaciones con el fin de dar sus clases de baile.</p>



<div id="accordion">
    
    <div id="collapseTwo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
    

    <p>Eduardo, Israel y Juan Carlos, coordinadores del espacio, vieron una excelente oportunidad en estos jóvenes para poner en práctica  algunos de los principios que guían el proyecto PILARES.</p>
    <p>Se habló con ellos sobre la importancia de continuar con sus estudios, que por diversas razones habían suspendido, y se les invitó a terminar su bachillerato con ayuda de nuestro programa; así, los coordinadores crearon una situación de ‘ganar-ganar’ para estos jóvenes amigos: se les brindarían las mejores condiciones para sus actividades de baile, y ellos se comprometieron a estudiar y concluir el nivel medio superior.</p>

    <p>Salsa, bachata, free style son los estilos que ahora enseñan como talleristas; sus habilidades, que adquirieron desde muy jóvenes, les permiten asegurar que cualquier ritmo de música moderna puede ser aprendido y enseñado por ellos. En las mañanas acuden a las ciberescuela donde se conectan a la plataforma virtual, revisan las actividades del bachillerato en línea que tienen para esa semana, y luego, planean sus horarios de trabajo y estudio. Cuando es necesario se conectan por las tardes aprovechando la flexibilidad en horario que les ofrece esta oferta educativa, sin embargo, esta posibilidad la toman como una responsabilidad.</p>
    <p>Lalo comenta que no dejan ninguna actividad para la mera hora, planea cada una y procura que, a más tardar el día viernes, ya estén concluidas sus tareas en la plataforma virtual, y así disfrutar de su fin de semana, el cual lo ocupan con sus actividades de baile dentro del PILARES.</p>

    <p>Alexis y Lalo se encuentran por concluir la cuarta semana del propedéutico del bachillerato en línea y mencionan que se sienten entusiasmados por las actividades, comentan que su facilitador del bachillerato en línea está al pendiente de sus dudas en la plataforma, además de que los docentes que laboran en el “Richard Wagner” les brindan las herramientas necesarias para realizar sus actividades de la mejor forma y de manera autónoma. </p>
    <p>Por su parte Israel, Juan Carlos y Eduardo nos comparten con emoción su sentir. Verdaderamente se empieza a incidir en la población. Este tipo de historias, entre otras que nos irán contando, confirman que PILARES se consolida como un espacio de inclusión, de participación y de referencia para la colonia Vallejo, en la alcaldía Gustavo A. Madero. 
    </p>
